#!/bin/bash

function createAirlines() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/airlines.airmile.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/airlines.airmile.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-airlines --tls.certfiles "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-airlines.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-airlines.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-airlines.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-airlines.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/airlines.airmile.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy airlines's CA cert to airlines's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/airlines.airmile.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem" "${PWD}/organizations/peerOrganizations/airlines.airmile.com/msp/tlscacerts/ca.crt"

  # Copy airlines's CA cert to airlines's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/airlines.airmile.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem" "${PWD}/organizations/peerOrganizations/airlines.airmile.com/tlsca/tlsca.airlines.airmile.com-cert.pem"

  # Copy airlines's CA cert to airlines's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/airlines.airmile.com/ca"
  cp "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem" "${PWD}/organizations/peerOrganizations/airlines.airmile.com/ca/ca.airlines.airmile.com-cert.pem"

  echo "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-airlines --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering user"
  set -x
  fabric-ca-client register --caname ca-airlines --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-airlines --id.name airlinesadmin --id.secret airlinesadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-airlines -M "${PWD}/organizations/peerOrganizations/airlines.airmile.com/peers/peer0.airlines.airmile.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/airlines.airmile.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/airlines.airmile.com/peers/peer0.airlines.airmile.com/msp/config.yaml"

  echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-airlines -M "${PWD}/organizations/peerOrganizations/airlines.airmile.com/peers/peer0.airlines.airmile.com/tls" --enrollment.profile tls --csr.hosts peer0.airlines.airmile.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/airlines.airmile.com/peers/peer0.airlines.airmile.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/airlines.airmile.com/peers/peer0.airlines.airmile.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/airlines.airmile.com/peers/peer0.airlines.airmile.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/airlines.airmile.com/peers/peer0.airlines.airmile.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/airlines.airmile.com/peers/peer0.airlines.airmile.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/airlines.airmile.com/peers/peer0.airlines.airmile.com/tls/server.key"

  echo "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-airlines -M "${PWD}/organizations/peerOrganizations/airlines.airmile.com/users/User1@airlines.airmile.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/airlines.airmile.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/airlines.airmile.com/users/User1@airlines.airmile.com/msp/config.yaml"

  echo "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://airlinesadmin:airlinesadminpw@localhost:7054 --caname ca-airlines -M "${PWD}/organizations/peerOrganizations/airlines.airmile.com/users/Admin@airlines.airmile.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/airlines/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/airlines.airmile.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/airlines.airmile.com/users/Admin@airlines.airmile.com/msp/config.yaml"
}

